package com.sasteyguru.pslscheduleandlivescores;

class winnerHelper {
    String year;
    String winner;
    String runner;


    public winnerHelper(String year, String winner, String runner) {
        this.year = year;
        this.winner = winner;
        this.runner = runner;

    }

    public String getYear() {
        return year;
    }
    public String getWinner() {
        return winner;
    }
    public String getRunner() {
        return runner;
    }



}
